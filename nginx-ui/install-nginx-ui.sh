### 2. NginxUI (Web Panel) Kurulumu

NginxUI aracını resmi betik yardımıyla sunucu servisi olarak (native) kurabilirsiniz:

```bash
bash <(curl -L -s https://raw.githubusercontent.com/0xJacky/nginx-ui/master/install.sh) install
```

Kurulum bittikten sonra web paneline `http://<Sunucu-IP>:9000` portundan giriş yapabilirsiniz. Varsayılan kullanıcı adı/şifre sistemi `admin`/`admin` dir. Girdiğiniz gibi şifreyi değiştirin.
